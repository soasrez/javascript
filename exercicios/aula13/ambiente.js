// Estrutura de Repetição com Teste Lógico no Final (DO WHILE)

var c = 1
do{
    console.log(`Passo ${c}`)
    c++
} while (c <= 5)

// Estrutura de Repetição com Teste Lógico no Início (WHILE)
var c = 1
while (c <= 500) {
    console.log(`Passo ${c}`)
    c++ // c = c + 1 / c recebe c + 1
}









